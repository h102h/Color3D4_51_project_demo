void PwmUp(uint a, uchar c)
{
	uchar i;
	if(c == 1)
		P3 = 0X00;
	else 
		P0 = 0X00;
	P2 = 0X00;
	i = table[a];
	Delay(i);
	if(c == 1)
		P3 = 0XFF;
	else
		P0 = 0XFF;
	P2 = 0X00;
	Delay(150-i);
} 

